				<h3>Idee</h3>
				Quizzenger wurde im Rahmen einer Studienarbeit an der Hochschule für Technik Rapperswil von <strong>Tobias Zahner</strong> und <strong>Oussama Zgheb</strong> entwickelt.
				<p>Quizzenger ist eine webbasierte Wissensdatenbank welche dem User ermöglicht für verschiedenste Themenbereich Quizzes durchzuführen.
				Das System soll sich selbst verwalten, durch ausgeklügelte Statistiken sowie dank der Community selbst.</p>
				<p>Quizzenger verfolgt die Idee einer offenen Community an der jeder Teilnehmen darf, deswegen ist es auch nahe liegend das Projekt mit einer Opensource Lizenz öffentlich zugänglich zu machen.</p>
				<p>Die Fragen werden entweder unterwegs auf einem Smartphone oder auf einem PC in einem Webbrowser beantwortet. Die Mobileseite ist für das Konsumieren des Inhaltes gedacht, die Desktopseite bietet zusätzliche Funktionen wie neue Fragen zu erfassen und gewisse administrative Tätigkeiten zu übernehmen
				Als Fragetypen ist im Rahmen unseres Projektes Single Choice Fragen gedacht.</p>
				<p>Bei der Architektur und Implementation wird viel Wert auf Flexibilität gelegt, sodass das Projekt ohne grossen Aufwand erweitert werden kann.</p>

				<h3>Ziel</h3>
				<p>Unser Ziel ist es einerseits eine erfolgreiche und spannende Studienarbeit durchzuführen, andererseits eine gut zu wartende Lernplattform zu erstellen.
				Diese Lernplattform soll sich mit der Zeit selbst verwalten und den Leuten eine effektive Möglichkeit bieten zu Lernen sowie eigenes Wissen weiterzugeben.</p>
				<p>Wir versuchen unser Bestes eine stabile und gut durchdachte Plattform zu erstellen, auf der im Rahmen einer weiteren Arbeit weitere Features und Verbesserunge eingebracht werden können.</p>
